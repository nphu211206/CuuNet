/**
 * Get world disasters by region
 */
export function getWorldDisastersByRegion(): Record<string, DisasterEvent[]> {
    const regions: Record<string, DisasterEvent[]> = {
        "Châu Á": [],
        "Châu Âu": [],
        "Châu Phi": [],
        "Châu Mỹ": [],
        "Châu Đại Dương": [],
    };

    for (const d of worldDisasters) {
        const lat = d.location.lat;
        const lng = d.location.lng;

        if (lng >= 25 && lng <= 180 && lat >= -10 && lat <= 55) {
            regions["Châu Á"].push(d);
        } else if (lng >= -25 && lng <= 45 && lat >= 35 && lat <= 70) {
            regions["Châu Âu"].push(d);
        } else if (lng >= -20 && lng <= 55 && lat >= -35 && lat <= 37) {
            regions["Châu Phi"].push(d);
        } else if (lng >= -170 && lng <= -35) {
            regions["Châu Mỹ"].push(d);
        } else {
            regions["Châu Đại Dương"].push(d);
        }
    }

    return regions;
}

/**
 * Get world disaster stats
 */
export function getWorldDisasterStats() {
    const byType: Record<string, number> = {};
    const bySeverity: Record<string, number> = {};
    let totalAffected = 0;

    for (const d of worldDisasters) {
        byType[d.type] = (byType[d.type] || 0) + 1;
        bySeverity[d.severity] = (bySeverity[d.severity] || 0) + 1;
        totalAffected += d.affectedPeople;
    }

    return {
        total: worldDisasters.length,
        byType,
        bySeverity,
        totalAffected,
    };
}