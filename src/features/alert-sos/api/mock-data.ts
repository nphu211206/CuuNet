let _idCounter = 0;
function generateId(prefix: string): string {
  _idCounter++;
  const ts = Date.now().toString(36);
  const rand = Math.random().toString(36).substring(2, 8);
  return `${prefix}-${ts}-${rand}-${_idCounter}`;
}

function randomPastDate(daysBack: number): string {
  const now = Date.now();
  const offset = Math.random() * daysBack * 24 * 60 * 60 * 1000;
  return new Date(now - offset).toISOString();
}

function randomFutureDate(daysAhead: number): string {
  const now = Date.now();
  const offset = Math.random() * daysAhead * 24 * 60 * 60 * 1000;
  return new Date(now + offset).toISOString();
}

