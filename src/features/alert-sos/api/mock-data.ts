function createSOSRequest(
  type: SOSType,
  severity: SOSEmergencyLevel,
  status: SOSStatus,
  province: string,
  daysAgo: number,
  peopleCount: number,
  hasChildren: boolean,
  hasElderly: boolean,
  isOffline: boolean,
  description: string,
): SOSRequest {
  const coords = PROVINCE_COORDS[province];
  const lat = coords.lat + (Math.random() - 0.5) * 0.1;
  const lng = coords.lng + (Math.random() - 0.5) * 0.1;
  const district = coords.districts[Math.floor(Math.random() * coords.districts.length)];
  const createdAt = randomPastDate(daysAgo);

  const timeline: SOSTimeline[] = [
    { status: "sent", timestamp: createdAt, note: "SOS được gửi" },
  ];

  if (status !== "queued" && status !== "sent") {
    timeline.push({ status: "delivered", timestamp: new Date(new Date(createdAt).getTime() + 30000).toISOString(), note: "Hệ thống đã nhận" });
  }
  if (["acknowledged", "dispatched", "resolved"].includes(status)) {
    timeline.push({ status: "acknowledged", timestamp: new Date(new Date(createdAt).getTime() + 120000).toISOString(), note: "Đội cứu hộ xác nhận", updatedBy: "Đội PCTT " + province });
  }
  if (["dispatched", "resolved"].includes(status)) {
    timeline.push({ status: "dispatched", timestamp: new Date(new Date(createdAt).getTime() + 300000).toISOString(), note: "Đội cứu hộ đang trên đường", updatedBy: "Đội PCTT " + province });
  }
  if (status === "resolved") {
    timeline.push({ status: "resolved", timestamp: new Date(new Date(createdAt).getTime() + 1800000).toISOString(), note: "Đã giải quyết", updatedBy: "Đội PCTT " + province });
  }

  const priorityBase = severity === "life_threatening" ? 100 : severity === "urgent" ? 70 : 40;
  const priorityScore = priorityBase + (hasChildren ? 10 : 0) + (hasElderly ? 10 : 0) + peopleCount * 2;

  return {
    id: generateId("SOS"),
    userId: generateId("USR"),
    location: {
      lat,
      lng,
      accuracy: Math.floor(Math.random() * 50) + 5,
      address: `${district}, ${province}`,
      province,
      district,
    },
    situation: {
      type,
      severity,
      peopleCount,
      description,
      hasChildren,
      hasElderly,
      hasDisabled: Math.random() > 0.8,
      isTrapped: type === "trapped" || Math.random() > 0.7,
      needsMedical: severity === "life_threatening" || Math.random() > 0.6,
    },
    contact: {
      name: `Người dùng ${Math.floor(Math.random() * 1000)}`,
      phone: `09${Math.floor(Math.random() * 100000000).toString().padStart(8, "0")}`,
    },
    status,
    timeline,
    createdAt,
    sentAt: status !== "queued" ? createdAt : undefined,
    deliveredAt: ["delivered", "acknowledged", "dispatched", "resolved"].includes(status) ? new Date(new Date(createdAt).getTime() + 30000).toISOString() : undefined,
    acknowledgedAt: ["acknowledged", "dispatched", "resolved"].includes(status) ? new Date(new Date(createdAt).getTime() + 120000).toISOString() : undefined,
    dispatchedAt: ["dispatched", "resolved"].includes(status) ? new Date(new Date(createdAt).getTime() + 300000).toISOString() : undefined,
    resolvedAt: status === "resolved" ? new Date(new Date(createdAt).getTime() + 1800000).toISOString() : undefined,
    isOffline,
    retryCount: isOffline ? Math.floor(Math.random() * 3) : 0,
    maxRetries: 10,
    priorityScore,
    estimatedResponseTime: Math.floor(Math.random() * 30) + 10,
    assignedTeam: status !== "queued" && status !== "sent" ? `Đội PCTT ${province}` : undefined,
  };
}

